# 袧械懈蟹胁械褋褌薪褘泄 褕懈褎褉邪褌芯褉

alphathit = "邪斜胁谐写械褢卸蟹懈泄泻谢屑薪芯锌褉褋褌褍褎褏褑褔褕褖褗褘褜褝褞褟邪斜胁谐写械褢卸蟹懈泄泻谢屑薪芯锌褉褋褌褍褎褏褑褔褕褖褗褘褜褝褞褟"
# 胁褌芯褉芯泄 邪谢褎邪胁懈褌 薪邪屑 薪褍卸械薪, 械谢褋懈 斜褍写械屑 褕懈褎褉芯胁邪褌褜 锌芯褋谢械写薪懈械 斜褍泻胁褘 邪谢褎芯胁懈褌邪
encrypt = input("袙胁械写懈褌械 泻褉懈锌褌芯谐褉邪屑屑褍: ")
key = int(input("Pleas enter a  key (number from 1-25): "))
encrypt = encrypt.lower() # lower 锌褉械胁褉邪褕邪械褌 胁褋械 斜褍泻胁褘 胁 屑邪谢械薪褜泻懈械(胁 薪懈蟹卸薪懈泄 褉械谐懈褋褌褉))
encrypted = ""
# 袦褘 斜褍写械屑 懈褋锌芯谢褜蟹芯胁邪褌褜 褑懈泻谢 for, 褔褌芯斜褘 芯斜褉邪褌懈褌褜褋褟 泻 泻邪卸写芯泄 斜褍泻胁械 锌芯 械械 薪芯屑械褉褍 懈 胁褘褔械褋谢懈褌褜 蟹邪褌械屑 薪芯胁褘泄 薪芯屑械褉
for letter in encrypt:
	position = alphathit.find(letter) # find() 芯斜褉邪褖邪械褌褋褟 泻 锌械褉械屑械薪薪芯泄 alphathit 写谢褟 锌芯懈褋泻邪 锌械褉胁芯谐芯 褋芯胁锌邪写械薪懈褟 懈  胁械褉薪械褌 锌芯褉褟写泻芯胁褘泄 薪芯屑械褉 褝褌芯泄 斜褍泻胁褘
	newPosition = position + key # 袣 懈薪写械泻褋褍 斜褍泻胁褘 + 泻谢褞褔
	if letter in alphathit: # 袩褉芯胁械褉泻邪 褋懈屑胁芯谢芯胁 
		encrypted = encrypted + alphathit[newPosition] # 袙 褝褌芯泄 褋褌褉芯泻械 薪邪褏芯写懈褌褜褋褟 薪芯胁邪褟 斜褍泻胁邪. 袩芯褋谢械 褋屑械褖械薪懈褟 芯薪邪 写芯斜邪胁谢褟械褌褋褟 泻 蟹邪褕懈褎褉芯胁邪薪芯屑褍 褋芯芯斜褖械薪懈褞
	else:
		encrypted = encrypted + letter # 袝褋谢懈 褋懈屑胁芯谢褘 薪械褌 (薪邪锌褉懈屑械褉, 蟹邪锌褟褌邪褟 懈谢懈 褌懈褉械), 褌芯 芯薪 写芯斜邪胁谢褟械褌褋褟 泻 蟹邪褕懈褎褉芯胁邪薪薪芯屑褍 褋芯芯斜褖械薪懈褞 泻邪泻 械褋褌褜
print("孝胁芯泄 褕懈褎褉: " + encrypted) 
